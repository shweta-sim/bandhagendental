              Botox är ett muskelavslappnande ämne framställt av Botulinum A-gift, ett förädlat protein framställt av Botulism bakterier. Ämnet har använts i medicinskt syfte sedan 50-talet och utvecklades för behandling av överaktivitet i muskulaturen runt ögat t.ex. vid okontrollerad blinkning.<br>
